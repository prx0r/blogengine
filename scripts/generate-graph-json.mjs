import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { load as yamlLoad } from "js-yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const contentDir = path.join(root, "content");
const publicDir = path.join(root, "public");

const files = [
  "phases.yaml", "assumptions.yaml", "risks.yaml",
  "correctives.yaml", "warnings.yaml", "traditions.yaml",
  "sourceCards.yaml", "edges.yaml",
];

const graph = {};
for (const file of files) {
  const filePath = path.join(contentDir, file);
  try {
    const raw = fs.readFileSync(filePath, "utf-8");
    const key = file.replace(".yaml", "");
    graph[key] = yamlLoad(raw) || [];
  } catch {
    graph[file.replace(".yaml", "")] = [];
  }
}

const outPath = path.join(publicDir, "graph-data.json");
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, JSON.stringify(graph, null, 2));
console.log(`  → Generated graph-data.json (${Object.keys(graph).length} keys)`);

const tsPath = path.join(root, "src", "atlas", "graph", "generated-data.ts");
const tsContent = `// Auto-generated by scripts/generate-graph-json.mjs\n// Do not edit manually\nimport type { Graph } from "./schema";\n\nexport const GRAPH_DATA: Graph = ${JSON.stringify(graph, null, 2)};\n`;
fs.writeFileSync(tsPath, tsContent);
console.log(`  → Generated generated-data.ts`);

// Generate essay/source data for Cloudflare Workers (no fs at runtime)
const essaysDir = path.join(contentDir, "essays");
const sourcesDir = path.join(contentDir, "sources");
let essays = [];
try { essays = fs.readdirSync(essaysDir).filter(f => f.endsWith(".json")).map(f => JSON.parse(fs.readFileSync(path.join(essaysDir, f), "utf-8"))); } catch {}
let sources = [];
try { sources = fs.readdirSync(sourcesDir).filter(f => f.endsWith(".json")).map(f => JSON.parse(fs.readFileSync(path.join(sourcesDir, f), "utf-8"))); } catch {};

// Include glossary essays alongside regular essays
const glossaryEssaysDir = path.join(contentDir, "glossary", "essays");
const glossarySourcesDir = path.join(contentDir, "glossary", "sources");
const allEssayFiles = loadJsonDir(glossaryEssaysDir);
const curatedEssays = allEssayFiles.filter(e => {
  const id = e.id || "";
  return !id.startsWith("bridge-") && !id.startsWith("t2-");
});
const allEssays = [...essays, ...curatedEssays];
const allSources = [...sources, ...loadJsonDir(glossarySourcesDir)];

const essayTsPath = path.join(root, "src", "lib", "generated-essays-data.ts");
const essayTsContent = `// Auto-generated by scripts/generate-graph-json.mjs\n// Do not edit manually\n\nexport const ESSAYS = ${JSON.stringify(allEssays, null, 2)};\nexport const SOURCES = ${JSON.stringify(allSources, null, 2)};\n`;
fs.writeFileSync(essayTsPath, essayTsContent);
console.log(`  → Generated generated-essays-data.ts (${allEssays.length} essays, ${allSources.length} sources)`);

// Generate glossary data (source material, essays with body blocks, concepts)
const glossaryDir = path.join(contentDir, "glossary");
const gSources = loadJsonDir(path.join(glossaryDir, "sources"));
const gEssays = loadJsonDir(path.join(glossaryDir, "essays"));
const gConcepts = loadJsonDir(path.join(glossaryDir, "concepts"));
const gArt = loadJsonDir(path.join(glossaryDir, "art"));

const glossaryTsPath = path.join(root, "src", "glossary", "generated-data.ts");
const glossaryTsContent = `// Auto-generated by scripts/generate-graph-json.mjs\n// Do not edit manually\n// @ts-nocheck\n\nexport const GLOSSARY_SOURCES = ${JSON.stringify(gSources, null, 2)};\nexport const GLOSSARY_ESSAYS = ${JSON.stringify(gEssays, null, 2)};\nexport const GLOSSARY_CONCEPTS = ${JSON.stringify(gConcepts, null, 2)};\nexport const GLOSSARY_ART = ${JSON.stringify(gArt, null, 2)};\n`;
fs.writeFileSync(glossaryTsPath, glossaryTsContent);
console.log(`  → Generated glossary/generated-data.ts (${gSources.length} sources, ${gEssays.length} essays, ${gConcepts.length} concepts, ${gArt.length} art)`);

function loadJsonDir(dir) {
  try {
    return fs.readdirSync(dir).filter(f => f.endsWith(".json")).map(f => JSON.parse(fs.readFileSync(path.join(dir, f), "utf-8")));
  } catch { return []; }
}
